#include "libft.h"

void	ft_strclr(char *s)
{
	int a;

	a = 0;
	if (s != NULL)
	{
		if (s[a] != '\0')
		{
			while (s[a])
			{
				s[a] = '\0';
				a++;
			}
		}
	}
}
